##Operasi Aritmatika
a = 10
b = 3
print("a","=",a)
print("b","=",b)

# Penjumlahan +
hasil = a + b
print(a,"+",b,"=",hasil)

# Pengurangan -
hasil = a - b
print(a,"-",b,"=",hasil)

# Perkalian *
hasil = a * b
print(a,"*",b,"=",hasil)

# Pembagian /
hasil = a / b
print(a,"/",b,"=",hasil)

# Eksponen / Perpangkatan **
hasil = a ** b
print(a,"**",b,"=",hasil)

# Modulus %
hasil = a % b
print(a,"%",b,"=",hasil)

#Floor Division //
hasil = a // b
print(a,"//",b,"=",hasil)

# Prioritas Operasi / Operational Predence
