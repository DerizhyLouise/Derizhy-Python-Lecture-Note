print("Berikut adalah program konversi suhu reamur")

reamur = float(input("Masukkan suhu dalam reamur: "))

celcius = 5 / 4 * reamur
fahreinheit = 9 / 4 * reamur + 32
kelvin = 5 / 4 * reamur + 273

print("Hasil konversi reamur ke celcius adalah",celcius,"C")
print("Hasil konversi reamur ke fahreinheit adalah",fahreinheit,"F")
print("Hasil konversi reamur ke kelvin adalah",kelvin,"K")