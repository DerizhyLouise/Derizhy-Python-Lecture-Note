
a = float(input("Masukkan nilai alas segitiga: "))
t = float(input("Masukkan nilai tinggi segitiga: "))
p = float(input("Masukkan nilai panjang persegi panjang: "))
l = float(input("Masukkan nilai lebar persegi panjang: "))
r = float(input("Masukkan nilai jari-jari lingkaran: "))

luasSegitiga = (1/2) * a * t
luasPersegi = p * l
luasLingkaran = (22/7) * r * r

print("Luas segitiga adalah", luasSegitiga)
print("Luas persegi panjang adalah", luasPersegi)
print("Luas lingkaran adalah", luasLingkaran)